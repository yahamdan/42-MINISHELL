#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	a;

	a = n;
	if (a < 0)
	{
		write(fd, "-", 1);
		a = a * (-1);
	}
	if (a >= 0 && a <= 9)
		ft_putchar_fd(a + '0', fd);
	else if (a > 9)
	{
		ft_putnbr_fd(a / 10, fd);
		ft_putnbr_fd(a % 10, fd);
	}
}
